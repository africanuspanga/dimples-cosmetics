// Recommendation algorithm
export function getRecommendations(answers: Record<string, string>) {
  const { goal, concern, area, budget } = answers

  // Calculate scores for each product
  const scoredProducts = products.map((product) => {
    let score = 0

    // Goal matching (highest weight)
    if (goal && product.tags.includes(goal)) score += 30

    // Concern matching
    if (concern && product.tags.includes(concern)) score += 25

    // Area matching
    if (area === "face" && product.tags.includes("face")) score += 20
    if (area === "body" && product.tags.includes("body")) score += 20
    if (area === "both") {
      if (product.tags.includes("face") || product.tags.includes("body")) score += 15
    }

    // Bonus for essential daily products
    if (product.tags.includes("daily")) score += 5
    if (product.tags.includes("essential")) score += 10

    return { ...product, score }
  })

  // Sort by score
  const sortedProducts = scoredProducts
    .filter((p) => p.score > 0)
    .sort((a, b) => b.score - a.score)

  // Determine number of products based on budget
  let limit = 2
  if (budget === "complete") limit = 4
  if (budget === "full") limit = 6

  // Get top products
  const recommendations = sortedProducts.slice(0, limit)

  // Always add sunscreen if not already included (it's essential!)
  const sunscreen = products.find((p) => p.id === 25)
  if (sunscreen && !recommendations.find((p) => p.id === 25)) {
    recommendations.push({ ...sunscreen, score: 10 })
  }

  return recommendations
}

